    private void validarLetras(char c, KeyEvent e) {
        if (Character.isDigit(c)) {
            JOptionPane.showMessageDialog(this, "No se permiten n√∫meros en este campo", "Entrada inv√°lida", JOptionPane.WARNING_MESSAGE);
            e.consume();
        }
    }

    private void validarNumeros(char c, KeyEvent e) {
        if (Character.isLetter(c)) {
            JOptionPane.showMessageDialog(this, "Campo de valores n√∫mericos", "Entrada inv√°lida", JOptionPane.WARNING_MESSAGE);
            e.consume();
        }
    }
